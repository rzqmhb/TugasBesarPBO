    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String nama = txtNamaPegawai.getText();
        String email = txtEmail.getText();
        String notlp = txtNoTelp.getText();
        String pass = txtPass.getText();
        
        if (!(nama.isEmpty() && email.isEmpty() && notlp.isEmpty() && pass.isEmpty())){
            Pegawai peg = new Pegawai();
            peg.setIdPegawai(Integer.parseInt(txtIdPegawai.getText()));
            peg.setNama(nama);
            peg.setEmail(email);
            peg.setNoTelp(notlp);
            peg.setPassword(pass);
            peg.save();
            
            tampilkanData();
        }
        else{
            JOptionPane.showMessageDialog(null, "lengkapi form terlebih dahulu");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

