    public void tampilkanData(){
        Object[] namaKolom = new Object[] {"ID","Nama","Email","No Telp"};
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(namaKolom);
        
        ArrayList<Pegawai> listPegawai = Pegawai.getAll();
        Object[] rowData = new Object[4];
        
        for (Pegawai peg : listPegawai){
            rowData[0] = peg.getIdPegawai();
            rowData[1] = peg.getNama();
            rowData[2] = peg.getEmail();
            rowData[3] = peg.getNoTelp();
            
            model.addRow(rowData);
        }
        tablePegawai.setModel(model);
    }
    
    public void cari(String keyword){
        Object[] namaKolom = new Object[] {"ID","Nama","Email","No Telp"};
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(namaKolom);
        
        ArrayList<Pegawai> listPegawai = Pegawai.search(keyword);
        Object[] rowData = new Object[4];
        
        for (Pegawai peg : listPegawai){
            rowData[0] = peg.getIdPegawai();
            rowData[1] = peg.getNama();
            rowData[2] = peg.getEmail();
            rowData[3] = peg.getNoTelp();
            
            model.addRow(rowData);
        }
        tablePegawai.setModel(model);
    }

